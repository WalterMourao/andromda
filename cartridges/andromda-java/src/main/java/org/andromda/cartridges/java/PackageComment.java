package org.andromda.cartridges.java;

/**
 * Enumeration for comments for package or compilation unit.
 * 
 * @author Lofi Dewanto
 * @since 1.3.0
 * @version 1.0.0
 */
public enum PackageComment {

	CONTENT_1("Generated by KissMDA - Andromda Java Cartridge - "
			+ "andromda-java-Cartridge."),

	CONTENT_2("DO NOT EDIT this file manually! "
			+ "All changes will be overwritten by next generation!");

	private String value;

	private PackageComment(String value) {
		this.value = value;
	}

	public String getValue() {
		return value;
	}
}
